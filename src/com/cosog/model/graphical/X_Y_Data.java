package com.cosog.model.graphical;

public class X_Y_Data {
	private Float XData;
	private Float YData;
	public X_Y_Data() {
		super();
	}
	public X_Y_Data(Float xData, Float yData) {
		super();
		XData = xData;
		YData = yData;
	}
	public Float getXData() {
		return XData;
	}
	public void setXData(Float xData) {
		XData = xData;
	}
	public Float getYData() {
		return YData;
	}
	public void setYData(Float yData) {
		YData = yData;
	}
	
}